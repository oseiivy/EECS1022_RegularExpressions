package com.example.testbed.chapter1;

public class Rectangle1
{
    public static int getArea(int w, int h)
    {
        int result = w * h;
        return result;
    }

    public static int getCircumference(int w, int h)
    {
        int result = w + h;
        result = result * 2;
        return result;
    }
}
